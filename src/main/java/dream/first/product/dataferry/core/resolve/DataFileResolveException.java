package dream.first.product.dataferry.core.resolve;

/**
 * 数据文件解析器
 */
public class DataFileResolveException extends Exception {

	private static final long serialVersionUID = -4073433646138599692L;

	public DataFileResolveException() {
		super();
	}

	public DataFileResolveException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataFileResolveException(String message) {
		super(message);
	}

	public DataFileResolveException(Throwable cause) {
		super(cause);
	}

}
