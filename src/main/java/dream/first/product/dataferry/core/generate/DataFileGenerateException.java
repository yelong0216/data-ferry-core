package dream.first.product.dataferry.core.generate;

/**
 * 数据文件生成异常
 */
public class DataFileGenerateException extends Exception {

	private static final long serialVersionUID = -2016711314643498824L;

	public DataFileGenerateException() {
		super();
	}

	public DataFileGenerateException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataFileGenerateException(String message) {
		super(message);
	}

	public DataFileGenerateException(Throwable cause) {
		super(cause);
	}

}
